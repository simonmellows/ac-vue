// build-floorplans.js
import fs from 'fs';
import path from 'path';

// Path to your SVG folder
const svgFolder = path.resolve('./public/floorplans');

// Path to output JS file
const outputFile = path.resolve('./src/data/floorplans.js');

// Read all SVG files
const files = fs.readdirSync(svgFolder).filter(f => f.endsWith('.svg'));

const registry = {};

for (const file of files) {
  const content = fs.readFileSync(path.join(svgFolder, file), 'utf-8')
    .replace(/\r?\n|\r/g, ''); // remove newlines to keep it compact
  const key = path.basename(file, '.svg');
  registry[key] = content;
}

// Write JS file
const jsContent = `// This file is auto-generated by build-floorplans.js
export default ${JSON.stringify(registry, null, 2)};
`;

fs.writeFileSync(outputFile, jsContent);
console.log(`Generated ${outputFile} with ${files.length} SVGs`);